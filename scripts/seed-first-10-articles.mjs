function richText(content) {
  return [
    {
      type: 'text',
      text: { content },
      annotations: {
        bold: false,
        italic: false,
        strikethrough: false,
        underline: false,
        code: false,
        color: 'default',
      },
    },
  ]
}

function paragraph(content) {
  return {
    object: 'block',
    type: 'paragraph',
    paragraph: { rich_text: richText(content) },
  }
}

function heading1(content) {
  return {
    object: 'block',
    type: 'heading_1',
    heading_1: { rich_text: richText(content) },
  }
}

function heading2(content) {
  return {
    object: 'block',
    type: 'heading_2',
    heading_2: { rich_text: richText(content) },
  }
}

function callout(content, emoji = '💡') {
  return {
    object: 'block',
    type: 'callout',
    callout: {
      rich_text: richText(content),
      icon: { type: 'emoji', emoji },
      color: 'default',
    },
  }
}

function bullets(items) {
  return items.map((item) => ({
    object: 'block',
    type: 'bulleted_list_item',
    bulleted_list_item: { rich_text: richText(item) },
  }))
}

function slugFromPath(path) {
  return path.split('/').filter(Boolean).at(-1) ?? ''
}

function article({ title, path, description, category = 'Теория', level = ['Junior'], readTime = 5, body }) {
  return {
    title,
    path,
    slug: slugFromPath(path),
    description,
    category,
    level,
    readTime,
    updatedAt: TODAY,
    status: 'Published',
    language: 'ru',
    children: body,
  }
}

